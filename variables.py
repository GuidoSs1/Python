# Las variables en python usas nomenclaturas dinamicas, por lo tanto, se pueden asignar a diferentes tipos de datos
a = 100 #Int
a = "Guido" #Str
a = 3.5 #Float
a = [10, "hola", 3.5] # List
a = {"palabra":"significado", "clave":"valor"} #Diccionario (Dict)
a = (10, "HOLA", 2.5) # Tuplas (Tup) Orden inmutable de objetos
a = {"a", "b"} # Sets (Set) coleccion de objetos no ordenados
a = True # Booleano
a = False # Booleano

print(a) # Mostrara False